class TestHBNBCommandPrompting(unittest.TestCase):
    """Unittests for testing the prompt and empty line handling of the HBNB command interpreter."""

    def test_prompt_string(self):
        """Test that the prompt string matches '(hbnb) '."""
        self.assertEqual("(hbnb) ", HBNBCommand.prompt)

    def test_empty_line(self):
        """Test that an empty line should not execute a command and should produce no output."""
        with patch("sys.stdout", new=StringIO()) as output:
            cmd = HBNBCommand()
            self.assertFalse(cmd.onecmd(""))
            self.assertEqual("", output.getvalue().strip())


